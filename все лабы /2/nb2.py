# Ввод первого числа (делимого)
a = int(input())  # Введите целое число a

# Ввод второго числа (делителя)
b = int(input())  # Введите целое число b

# Проверка деления на ноль
if b == 0:
    # Если делитель равен нулю, выводим сообщение об ошибке
    print("Деление на ноль")
else:
    # Если делитель не ноль, выполняем целочисленное деление и выводим результат
    print(a // b)  # Вывод результата деления a на b (целая часть)
