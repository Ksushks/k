def divide(a, b):
    # Проверка деления на ноль
    if b == 0:
        # Возвращаем сообщение об ошибке
        return "Деление на ноль"
    else:
        # Возвращаем результат 
        return a // b

# Функция сложения двух чисел
def add(a, b):
    # Возвращаем сумму
    return a + b

# Функция вычитания двух чисел
def subtract(a, b):
    # Возвращаем разность 
    return a - b

# Функция умножения 
def multiply(a, b):
    # Возвращаем произведение 
    return a * b
